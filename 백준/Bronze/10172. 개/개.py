# 개 ASCII 아트를 출력하는 문제
# 역슬래시(\)는 이스케이프 문자이므로 화면에 \를 찍으려면 \\처럼 두 번 써야 합니다.

print("|\\_/|")        # -> 첫 줄: |\_/|
print("|q p|   /}")    # -> 둘째 줄: |q p|   /}
print('( 0 )"""\\')    # -> 셋째 줄: ( 0 )"""\   (마지막 \를 출력하기 위해 \\ 또는 따옴표 조합 사용)
print('|"^"`    |')    # -> 넷째 줄: |"^"`    |
print("||_/=\\\\__|")  # -> 다섯째 줄: ||_/=\\__|
